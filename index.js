// add function
function add (a, b) {
    return a + b;
}
// subtract function
function subtract (a, b) {
    return a - b;
}
// multiplication function
function multiply (a, b){
    return a * b;
}
// division function
function divide (a, b){
    return a / b;
}
// increment
function increment(a) {
    a ++;
    return a;
}
// decrement
function decrement(a) {
     a --;
    return a;
}
// parseint
function makeInt(a) {
    let number = parseInt(a, 10);
    return number;
}
// parsefloat
function preserveDecimal(a) {
   let number = parseFloat(a)
   return number;
}
